package com.plantmanager.entity.enums;

/**
 * Light exposure requirement for a plant.
 * Indicates optimal lighting conditions.
 */
public enum Exposure {
    /** Full sun exposure (6+ hours direct sunlight) */
    SUN,
    /** Full shade (no direct sunlight) */
    SHADE,
    /** Partial shade (2-4 hours direct sunlight) */
    PARTIAL_SHADE
}
